//[0, limit)
int getRand(int limit){
    unsigned long long r = 0;

    for (int i = 0; i < 5; ++i) {
        r = (r << 15) | (rand() & 0x7FFF);
    }
    return (r & 0xFFFFFFFFFFFFFFFFULL) % limit;
}
//[l, r)
int getRand(int l, int r){
    int x = getRand(r - l);
    return x + l;
}
